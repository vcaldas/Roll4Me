# Força
# É o poder puro e bruto de um personagem. Ela indica quanto peso um personagem é capaz de levantar, quanto ele pode empurrar e com qual força ele pode atingir um outro personagem ou objeto. A Característica Força é adicionada à parada de dano de um
# personagem quando ele atinge um personagem em combate corpo-acorpo. Ela também é usada quando o personagem deseja quebrar, levantar ou carregar alguma coisa, ou quando deseja pular grandes distâncias.
#
# Especializações: Mãos Fortes, Braços Poderosos, Reservas de Força, Punhos de Ferro
#
# Pontos:
# 1 Fraco: Você pode levantar 20 kg.
# 2 Médio: Você pode levantar 50 kg.
# 3 Bom: Você pode levantar 125 kg.
# 4 Excepcional: Você pode levantar 200 kg.
# 5 Extraordinário: Você pode levantar 325 kg e esmagar crânios como uvas.
#
# Destreza
# Mede a habilidade física geral de um personagem. Ela abrange a velocidade, agilidade e a rapidez geral do personagem, assim como sua agilidade em manipular objetos com
# controle e precisão. Também incluídos sob o título Destreza estão a coordenação visual e motora, os reflexos e a graciosidade dos movimentos.
#
# Especializações: Flexibilidade, Velocidade, Reflexos Felinos, Reflexos Rápidos
#
# Pontos
# 1 Fraco: Você é desajeitado e deselegante. Abaixe essa arma antes que você se machuque.
# 2 Médio: Você não é nenhum estúpido, mas também não é um bailarino.
# 3 Bom: Você possui algum potencial atlético.
# 4 Excepcional: Você poderia ser um acrobata se quisesse.
# 5 Extraordinário: Seus movimentos são fluidos e hipnóticos, quase sobrenaturais.
#
# Vigor
# Reflete a saúde, a resistência e o poder de recuperação do personagem. Ela indica por quanto tempo um personagem pode se esforçar e quanta punição física ele é capaz de
# suportar antes de sofrer traumas físicos. O Vigor também inclui um pouco de força psicológica, indicando a determinação e a tenacidade de um personagem em não desistir.
#
# Especializações: Incansável, Determinado, Resistente, Resoluto
#
# Pontos
# 1 Fraco: Você se machuca com um vento forte.
# 2 Médio: Você tem saúde mediana e aguenta um soco ou dois.
# 3 Bom: Você está em boa forma e raramente fica doente.
# 4 Excepcional: Você pode correr — e talvez vencer — em qualquer maratona que escolher.
# 5 Extraordinário: Você possui a constituição de um verdadeiro Hércules.
#
# Sociais
#
# Apesar de sua predisposição à solidão, os vampiros usam a sociedade humana como instrumento para levar adiante os seus planos.
#
# Os Atributos Sociais descrevem a aparência, o charme e a habilidade de um personagem em interagir com a sociedade. Estas Características são fundamentais na determinação da primeira impressão, dinâmica pessoal e relacionamento do personagem com os
# outros indivíduos.
#
# Carisma
# É a habilidade de um personagem em atrair e agradar os outros através de sua personalidade. O Carisma é usado quando um personagem tenta ganhar a simpatia de outro ou encorajá-lo a confiar nele. O Carisma não indica que o personagem é necessariamente eloquente ou habilidoso em intimidação. Mas é o simples poder do charme e da influência de um personagem.
#
# O Carisma descreve a habilidade de um personagem de convencer os outros a concordarem com o seu ponto de vista.
#
# Especializações: Fala Mansa, Distinto, Urbano, Engraçado, Eloquente, Gracioso
#
# Pontos
# 1 Fraco: Pare de cutucar o nariz.
# 2 Médio: Você é razoavelmente agradável e tem muitos amigos.
# 3 Bom: As pessoas confiam implicitamente em você.
# 4 Excepcional: Você tem um magnetismo pessoal significativo.
# 5 Extraordinário: Culturas inteiras poderiam seguir a sua liderança.
#
# Manipulação
# Mede a habilidade de auto-expressão de um personagem com o objetivo de fazer com que os outros compartilhem de suas perspectivas ou sigam seus caprichos. Em resumo, é fazer com que os outros façam o que você quer. A Manipulação é usada no jogo quando um personagem tenta influenciar ou direcionar sutilmente o comportamento de outros. A Manipulação é usada para enganar, blefar, usar de lábia e despachar outros personagens. Se o personagem em questão gosta ou não de ser manipulado é irrelevante (essa é a diferença entre a Manipulação e o Carisma); um motivador habilidoso pode até mesmo se aproveitar das habilidades daqueles que o odeiam.
#
# A Manipulação é um negócio perigoso, especialmente entre os Membros (apesar desta ser sua principal moeda). Falhas em tentativas de Manipulação frequentemente provocam o ódio dos que estavam para ser enganados. Falhas Críticas em jogadas de Manipulação podem adicionar nomes na lista de inimigos de um personagem.
#
# As pessoas são manipuladas todos os dias e normalmente ignoram isso. ("Você não quer ir ao mercado para mim?") Se alguém chamar atenção sobre o fato, contudo, a maioria das pessoas se tornam bastante defensivas. A Manipulação pode ser a ferramenta
# mais poderosa do repertório de um Membro, mas uma falha pode ser desastrosa. Personagens com altos níveis de Manipulação frequentemente não têm a confiança daqueles que os cercam.
#
# Especializações: Convincente, "Meu Deus, como eu sou malandro", Sedutor, Bom Argumentador
#
# Pontos
# 1 Fraco: Uma pessoa de poucas (e frequentemente ineficientes) palavras.
# 2 Médio: Você consegue enganar algumas pessoas, algumas vezes, como qualquer outra pessoa.
# 3 Bom: Você nunca paga o preço integral.
# 4 Excepcional: Você poderia ser um político ou o líder de um culto.
# 5 Extraordinário: "E claro que eu vou dizer ao Príncipe que fui eu quem tentou fincar a estaca!"
#
# Aparência
# É tanto "mais" como "menos" do que palavras - ela apela para os níveis mais profundos do psíquico, e portando, molda tanto as primeiras impressões como a natureza das memórias depois delas. Não importa quão "mente aberta" seja uma pessoa, não importa quão veementemente ele diga, "a personalidade é mais importante do que a aparência", as aparências continuam sendo levadas em consideração.
#
# Esta Característica não é usada apenas para fazer com que amantes potenciais notem seus gestos através de um salão lotado. Em situações onde a primeira impressão é fundamental, ou que envolvam pessoas que consideram a Aparência algo muito importante, um personagem não pode ter mais dados em uma parada Social do
# que o seu nível em Aparência. Portanto, é criticamente importante que você tenha boa aparência ou conheça melhor as pessoas antes de tentar convencê-las a bombardear o refúgio do Justicar.
#
# Pontos
# 1 Fraco: Feio como um macaco.
# 2 Médio: Você não se destaca na multidão, nem para melhor e nem para pior.
# 3 Bom: Estranhos se oferecem para lhe pagar uma bebida.
# 4 Excepcional: Você é atraente o suficiente para ser um modelo e as pessoas freqüentemente lhe dizem isso.
# 5 Extraordinário: Você causa tanto ciúmes insanos como reverências beatas.
#
# Mentais
#
# Os Atributos Mentais definem as capacidades cerebrais de um personagem, incluindo aspectos como a memória, inteligência, consciência do que o cerca e a habilidade de pensar, aprender e reagir.
#
# Percepção
# Mede a habilidade do personagem de observar seus arredores. Isso pode envolver um esforço consciente, como procurar em uma área, mas é mais frequentemente intuitivo, conforme os sentidos aguçados do personagem notam que algo está fora do normal. A Percepção é uma sensibilidade aos arredores do personagem e raramente está presente nos cínicos e exaustos (que já viram tudo isso antes).
#
# A Percepção é usada para determinar se o personagem entende ou não uma dada situação ou se ele detecta um estímulo no ambiente. Ela pode adertir um personagem da presença de uma emboscada, ajudá-lo a identificar uma metáfora, distinguir uma
# pista em uma pilha de lixo ou descobrir qualquer outro detalhe escondido ou ignorado, sejam eles físicos ou não.
#
# Especializações: Atento, Introspectivo, Cuidadoso, Penetrante, Perspicaz, Experiente
#
# Pontos
# 1 Fraco: Talvez você seja absurdamente distraído, talvez meramente um cabeça-de-vento; de qualquer modo, até mesmo os detalhes mais óbvios o enganam.
# 2 Médio: Você desconhece os detalhes, mas está ciente dotodo.
# 3 Bom: Você distingue temperamentos, texturas e pequenas mudanças no ambiente.
# 4 Excepcional: Praticamente nada lhe passa desapercebido.
# 5 Extraordinário: Você observa instantaneamente coisas que são quase imperceptíveis aos sentidos humanos.
#
# Inteligência
# Se refere à compreensão do personagem sobre os fatos e conhecimentos. Mais importante do que isso, contudo, ele governa a habilidade de um personagem de argumentar, resolver problemas e avaliar as situações. O termo Inteligência é quase
# um erro, pois o Atributo também abrange o senso crítico e a flexibilidade de pensamento.
#
# A Inteligência não inclui a compreensão, a sabedoria ou o bom senso, pois estes compõem os traços da personalidade do personagem e não as suas Características. Até mesmo os personagens mais espertos podem ser tolos o bastante para abrirem a boca na hora errada ou insensatos demais para perceberem que os capangas que querem as chaves do carro não estão com boas intenções.
#
# Personagens com um baixo nível de Inteligência não são necessariamente estúpidos (contudo, eles podem ser), mas apenas não foram educados ou são pessoas de mentes simples. Da mesma forma, personagens com alta Inteligência não são todos como Einstein: eles podem ser melhores em memorizar rotas ou terem um juízo
# particularmente aguçado.
#
# Especializações: Literato, Criativo, Analítico, Resolvedor de Problemas, Autoridade em Áreas Específicas
#
# Pontos
# 1 Fraco: Não é a faca mais afiada da gaveta (QI 80).
# 2 Médio: Esperto o suficiente para perceber que é normal (QI 100).
# 3 Bom: Mais esclarecido que as massas (QI 120).
# 4 Excepcional: Você não é apenas brilhante, você é absolutamente brilhante (QI 140).
# 5 Extraordinário: Um verdadeiro gênio (QI 160).
#
# Raciocínio
# Mede a habilidade de um personagem de pensar rapidamente e reagir com velocidade a certas situações. Ela também reflete a esperteza geral do personagem. Personagens com baixos níveis de Raciocínio são lentos e mentalmente letárgicos, ou talvez, crédulos e inocentes. Em contrapartida, personagens com altos níveis de Raciocínio quase sempre têm um plano e se adaptam a novos ambientes com rapidez notável. Personagens com altos níveis de Raciocínio também conseguem se manter calmos em situações de tensão.
#
# Especializações: Ganhar a Dianteira, Orador Vigoroso, Mudar de Estratégia, Emboscadas
#
# Pontos
# 1 Fraco: Puxe meu dedo.
# 2 Médio: Você sabe quando apostar e quando passar no pôquer.
# 3 Bom: Você raramente é pego de surpresa ou fica sem palavras.
# 4 Excepcional: Você é uma daquelas pessoas que fazem os outros pensar, "ooh, eu deveria ter dito isso ..." no dia seguinte.
# 5 Extraordinário: Você pensa e responde quase tão velozmente quanto é capaz de agir.